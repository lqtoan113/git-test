// Hàm tạo độ trễ
function delay(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

// Hàm thực hiện Insertion Sort với theo dõi
async function insertionSort(arr) {
    console.log("Mảng ban đầu: " + arr.join(", ")); // Xuất mảng ban đầu

    let n = arr.length;
    for (let i = 1; i < n; i++) {
        let key = arr[i];
        let j = i - 1;

        // Chèn `key` vào vị trí đúng trong phần đã sắp xếp
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j--;
            // console.log(`Dịch chuyển: ${arr.join(", ")}`);
            // await delay(10); // Chờ 0.5 giây
        }
        arr[j + 1] = key;
        console.log(`Chèn: ${key} vào vị trí ${j + 1}`);
        // console.log(`Chèn: ${key} vào vị trí ${j + 1} | Mảng hiện tại: ${arr.join(", ")}`);
    }
    console.log("Mảng sau khi sắp xếp: " + arr.join(", "));
}

